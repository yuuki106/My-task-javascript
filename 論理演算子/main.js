
// 1. AND(&&)の条件を作成し、その処理に入るよう作ってください。(elseの処理も作成してください)
var mathScore = 80;
var englishScore = 80;
if (mathScore >= 80 && englishScore >= 80) {
  console.log("よく出来ました!!");
} else {
  console.log("追試です");
};

// 2. OR(||)の条件を作成し、その処理に入るよう作ってください。(elseの処理も作成してください)
var money = 250;
var point = 100;
var price = 250;
if (money >= price || point >= price) {
  console.log("商品を購入できます。");
} else {
  console.log("商品を購入できません");
};

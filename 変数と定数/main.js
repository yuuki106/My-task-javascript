// 1. var、let、constの使用についてまとめてください。
// var:変数の再代入、再宣言が行える。故に予期せぬバグが起こりやすい。
// let:変数の再代入は出来るが、再宣言は出来ない。varよりか不便に感じるが、予期せぬ再宣言が行われるのを防げる。
// const:定数。再代入、再宣言ともに不可(配列やオブジェクトの操作は可)。一見不便に感じるが、予期せぬ再代入、再宣言を防げる為、より安全にコーディング出来る。

// 2. なぜvarを使わない理由をまとめてください。
// 容易に変数の再代入、再宣言が行える為。一見便利に感じるが容易に再代入、再宣言が出来てしまうと、予期せぬ再代入、再宣言をしてしまう可能性がありバグが起こりやすい。故にvarを使用しない。

// 3. 定数とは?
//ある一定の値。なおその値を変更することは出来ない。JavaScriptだとconstが該当する。
